  static int maxSum=0;
  public static int getMaximumGold(int[][] grid) {
    boolean[][] maze=new boolean[grid.length][grid[0].length];

    for(int i = 0; i < grid.length; i++){
      for (int j = 0; j < grid[i].length; j++) {
        getMaximumGoldHelper(grid, maze, i, j, 0);
      }
    }

    return maxSum;
  }

  private static void getMaximumGoldHelper(int[][] grid, boolean[][] maze, int i, int j, int currSum) {
    if(currSum>maxSum){
      maxSum=currSum;
    }

    if(i>grid.length-1 || i<0 || j<0 || j>grid[0].length - 1) return;

    if(grid[i][j]==0){
      return;
    }

    if(!maze[i][j]){
      maze[i][j] = true;
      currSum+=grid[i][j];

      getMaximumGoldHelper(grid, maze,i-1,j,currSum);
      getMaximumGoldHelper(grid, maze,i+1,j,currSum);
      getMaximumGoldHelper(grid, maze,i,j-1,currSum);
      getMaximumGoldHelper(grid, maze,i,j+1,currSum);

      maze[i][j] = false;
      currSum -= grid[i][j];
    }


  }

