  public static String inverse(String s) {
    StringBuilder str=new StringBuilder();

    for(int i=0; i<s.length(); i++){
      if(s.charAt(i)=='0'){
        str.append('1');
      }else{
        str.append('0');
      }
    }
    return str.toString();
  }

  public static String reverse(String s) {
    StringBuilder str=new StringBuilder();

    for(int i=0; i<s.length(); i++){
      str.append(s.charAt(s.length()-1-i));
    }

    return str.toString();
  }

