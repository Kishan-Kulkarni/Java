public boolean helper( boolean[][] maze, ArrayList<Character> list, char[][] board, String word, int r, int c, int i){
  if(r==board.length){
      return i==word.length();
  }
  
  if(c==board[0].length){
      return helper(maze, list, board, word,  r+1, 0, i);
  }
  
  if(i==word.length())
      return compare(list , word);
  
  if(board[r][c]==word.charAt(i) && !maze[r][c]){
      list.add(word.charAt(i));
      maze[r][c]=true;
      
      if(r>0){
          return helper(maze,list, board, word, r-1, c, i+1);
      }
      
      
      if(c>0){
          return helper(maze, list, board, word, r, c-1, i+1);
      }
      
      
      
      if(c<board[0].length-1){
          return helper(maze, list, board, word, r, c+1, i+1);
      }
      
      if(r<board.length-1){
          return helper(maze, list, board, word, r+1, c, i+1);
      }
      
      
      list.remove(list.size()-1);
      maze[r][c]=false;
  }
  
  return helper(maze, list, board,  word, r, c+1, i);
}

