  private static boolean checkParanthesis(String s) {
    if(s.isEmpty()){
      return true;
    }

    char ch=s.charAt(0);

    int i=-1;

    if(ch=='('){
       i=findParanthesis(s,ch);
    }else if(ch=='{'){
      i=findParanthesis(s,ch);
    }else if(ch=='['){
      i=findParanthesis(s,ch);
    }

    if(i==-1){
      return false;
     }

     if(i==s.length()-1){
      return checkParanthesis(s.substring(1, i));
     }else{
      return checkParanthesis(s.substring(i+1)) && checkParanthesis(s.substring(0, i+1));
     }
    
  }

  private static int findParanthesis(String s, char ch) {

    int found=-1;

    if(ch=='('){
      for(int i=0; i<s.length(); i++){
        if(s.charAt(i)==')'){
          found=i;
        }
      }
    }else if(ch=='{'){
      for(int i=0; i<s.length(); i++){
        if(s.charAt(i)=='}'){
          found=i;
        }
      }
    }else{
      for(int i=0; i<s.length(); i++){
        if(s.charAt(i)==']'){
          found=i;
        }
      }
    }
    return found;
  }

