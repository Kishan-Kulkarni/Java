  public static int rbs(int[] arr, int s , int e, int target){

    if(s>e){
      return -1;
    }

    int m=s+(e-s)/2;

    if(arr[m]==target){
      return m;
    }

    if(m>0 && m<arr.length-1 && arr[m]>arr[m+1] && arr[m]>arr[m-1]){
      if(target>=arr[s]){
        return rbs(arr, s, m-1, target);
      }else{
        return rbs(arr, m+1, e, target);
      }
    }

    if(arr[m]<target){
      return rbs(arr, m+1, e, target);
    }else{
      return rbs(arr, s, m-1, target);
    }
  } 

