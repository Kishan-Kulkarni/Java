  public  int longestPath(int[][] grid ,int x, int y){
    boolean[][] maze=new boolean[grid.length][grid[0].length];

    longestPathHelper(grid, maze, 0, 0, 0, x, y);
    return bigPath;
  }

  public  void longestPathHelper(int[][] grid, boolean[][] maze, int i, int j, int pathLength, int x, int y) {
    if(i==x && j==y){
      return;
    }

    if(pathLength>bigPath){
      bigPath=pathLength;
    }

    if(i<0 || i>grid.length-1 || j<0 || j>grid[0].length-1){
      return;
    }

    if(grid[i][j]==0){
      return;
    }



    if(!maze[i][j]){
      maze[i][j]=true;
      pathLength=pathLength+1;

      longestPathHelper(grid,maze,i+1,j,pathLength,x,y);
      longestPathHelper(grid,maze,i-1,j,pathLength,x,y);
      longestPathHelper(grid,maze,i,j+1,pathLength,x,y);
      longestPathHelper(grid,maze,i,j-1,pathLength,x,y);

      maze[i][j]=false;
      pathLength-=1;
    }
  }



